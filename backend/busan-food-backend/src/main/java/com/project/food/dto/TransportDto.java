package com.project.food.dto;

public class TransportDto {

    private String subwayText;
    private String busText;
    private String parkingText;

    public String getSubwayText() {
        return subwayText;
    }

    public void setSubwayText(String subwayText) {
        this.subwayText = subwayText;
    }

    public String getBusText() {
        return busText;
    }

    public void setBusText(String busText) {
        this.busText = busText;
    }

    public String getParkingText() {
        return parkingText;
    }

    public void setParkingText(String parkingText) {
        this.parkingText = parkingText;
    }
}